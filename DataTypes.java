package Basics;
public class DataTypes {
	public static void main(String[] args) {
		
		
	int i;//integer - declaration
	i=123;
	int j=10;//initialization
	
	int a=10; //integer to store numbers up to 32bit size
	long l=123; //store 64bit size
	
	float f=12.34f;  //to store 2 decimals and up to 32bit
	double d=123.4567; //more than 2 decimals
	
	char c='#';
	char c1='R';
	
	String st="Java session";
	String st1="This is our first Phase 2 Session";
	
	
	boolean b=true;
	
	
	System.out.println(i);
	System.out.println(j);
	System.out.println(a);
	System.out.println(l);
	System.out.println(f);
	System.out.println(d);
	System.out.println(c);
	System.out.println(c1);
	System.out.println(st);
	System.out.println(st1);
	System.out.println(b);
	}
}
